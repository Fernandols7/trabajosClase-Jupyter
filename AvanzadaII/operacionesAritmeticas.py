# Programa que hace operaciones atritmeticas basicas 

a=10;
b=5;

# Suma
suma = a + b
print(f"{a} + {b} = {suma}")

# Resta
resta = a - b
print(f"{a} - {b} = ",resta)

# Multiplicacion
multi = a * b
print(f"{a} * {b} = ",multi)

# Division 
division = a / b
print("Division = ",division)

